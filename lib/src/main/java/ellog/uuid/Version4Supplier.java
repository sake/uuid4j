package ellog.uuid;

public class Version4Supplier extends StandardUUIDSupplierBase {
	public Version4Supplier() {
		super(StandardVersion.RANDOM);
	}
	@Override
	public StandardUUID get() {
		return builder.setRandomTimestamp()
			.setRandomClockSequence()
			.setRandomNode()
			.build();
	}
}
